package clases;

public class Calculadora {
    public static int sumarEnteros(int a, int b) {
        return a + b;
    }

    public static int sumarEnteros(int a, int b, int c) {
        return a + b + c;
    }

    public static int sumarEnteros(int a, double b) {
        return a + (int) b;
    }
}
